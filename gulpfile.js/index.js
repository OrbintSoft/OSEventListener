const clean = require('./clean');
const gulp = require('gulp');
const lint = require('./lint');
const compile = require('./compile');
const test = require('./test');
const deploy = require('./deploy');

exports.clean = gulp.parallel(clean.dist, clean.packs, clean.testResults);
exports.lintSsrc = lint.src;
exports.lintTests = lint.tests;
exports.lintGulp = lint.gulp;
exports.lint = gulp.parallel(lint.src, lint.tests, lint.gulp);
exports.compileEs = compile.compileEs;
exports.compileUmd = compile.compileUmd;
exports.compileAmd = compile.compileAmd;
exports.compileCommonJs = compile.compileCommonJs;
exports.compileSystem = compile.compileSystem;
exports.compileTypescript = gulp.parallel(compile.compileEs, compile.compileUmd, compile.compileAmd, compile.compileCommonJs, compile.compileSystem);
exports.bundle = compile.createBundle;
exports.compile = gulp.series(exports.compileTypescript, exports.bundle);
exports.startSampleServer = test.startSampleServer;
exports.test =  gulp.series(test.runTests, test.checkDistBuild);
exports.coverage = test.runCoverage;
exports.deploy = deploy.publish;